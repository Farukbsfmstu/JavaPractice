
package practicejava;


public class AssignOparator {
    public static void main(String[] args){
        
      int x=50,y=10 ;
      
      x+=y;
      System.out.println("addition of x is:"+x);
      
      x-=y;
      System.out.println("Substrtion of x:"+x);
      
      x*=y;
      System.out.println("multipliccation of x:"+x);
      
      x/=y;
      System.out.println("division of x:"+x);
      
      x%=y;
      System.out.println("modulo division :"+x);
      
    }
}
